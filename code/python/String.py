# Integer
integer_variable = 10
print("Integer Variable:", integer_variable, "Type:", type(integer_variable))

# Float
float_variable = 3.14
print("Float Variable:", float_variable, "Type:", type(float_variable))

# String
string_variable = "Hello, world!"
print("String Variable:", string_variable, "Type:", type(string_variable))

# Boolean
boolean_variable = True
print("Boolean Variable:", boolean_variable, "Type:", type(boolean_variable))

# List
list_variable = [1, 2, 3, 4, 5]
print("List Variable:", list_variable, "Type:", type(list_variable))

# Tuple
tuple_variable = (1, 2, 3, 4, 5)
print("Tuple Variable:", tuple_variable, "Type:", type(tuple_variable))

# Dictionary
dictionary_variable = {'a': 1, 'b': 2, 'c': 3}
print("Dictionary Variable:", dictionary_variable, "Type:", type(dictionary_variable))

# Set
set_variable = {1, 2, 3, 4, 5}
print("Set Variable:", set_variable, "Type:", type(set_variable))

# NoneType
none_variable = None
print("None Variable:", none_variable, "Type:", type(none_variable))
